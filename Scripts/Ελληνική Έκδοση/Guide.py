def get_all_scripts():
    """Επιστρέφει μια επίπεδη λίστα όλων των σκριπτ σε όλες τις κατηγορίες."""
    all_scripts = []
    for category, tools in GUIDE_DATA.items():
        for script_name in tools.keys():
            all_scripts.append((category, script_name))
    return all_scripts

def get_filtered_list(all_items, query, search_in_categories=False):
    """Φιλτράρει τη λίστα με βάση το ερώτημα αναζήτησης."""
    if not query:
        return all_items
    
    query = query.lower()
    filtered = []
    
    if search_in_categories:
        # Αναζήτηση σε ονόματα κατηγοριών
        return [item for item in all_items if query in item.lower()]
    else:
        # Αναζήτηση σε σκριπτ (κατηγορία, όνομα_σκριπτ)
        return [(cat, script) for cat, script in all_items 
                if query in script.lower() or query in cat.lower()]

def show_category_tools(stdscr, category_name):
    """Εμφανίζει εργαλεία για μια συγκεκριμένη κατηγορία."""
    tools = list(GUIDE_DATA[category_name].keys())
    current_selection = 0
    scroll_offset = 0
    search_query = ""
    search_mode = False

    while True:
        stdscr.clear()
        h, w = stdscr.getmaxyx()
        
        draw_header(stdscr, f"Εργαλεία {category_name}", search_query)
        if not search_mode:
            draw_footer(stdscr, "main")
        else:
            # Σχεδίαση υποσέλιδου προτροπής αναζήτησης
            prompt = f" ΑΝΑΖΗΤΗΣΗ: {search_query}_ "
            safe_addstr(stdscr, h - 1, 0, " " * w, curses.color_pair(4))
            safe_addstr(stdscr, h - 1, max(0, (w - len(prompt)) // 2), prompt, curses.color_pair(4))
        
        # Υπολογισμός ορατής περιοχής
        menu_start_y = 4
        max_visible_items = max(1, h - menu_start_y - 2)
        
        # Προσαρμογή offset κύλισης
        if current_selection < scroll_offset:
            scroll_offset = current_selection
        elif current_selection >= scroll_offset + max_visible_items:
            scroll_offset = current_selection - max_visible_items + 1
            
        # Σχεδίαση Στοιχείων Μενού
        for i in range(max_visible_items):
            idx = scroll_offset + i
            if idx >= len(tools):
                break
                
            y = menu_start_y + i
            tool_name = tools[idx]
            
            display_text = format_list_item(tool_name, category_name)
            x = 2 
            
            if idx == current_selection:
                safe_addstr(stdscr, y, x, display_text, curses.color_pair(7) | curses.A_BOLD)
            else:
                safe_addstr(stdscr, y, x, display_text, curses.color_pair(8))
        
        # Μήνυμα κενής λίστας
        if not tools:
            safe_addstr(stdscr, menu_start_y, 2, "Δεν βρέθηκαν εργαλεία σε αυτήν την κατηγορία.", curses.color_pair(8))

        # Δείκτες Κύλισης
        if scroll_offset > 0:
             safe_addstr(stdscr, menu_start_y - 1, w//2, "^", curses.A_BOLD)
        if scroll_offset + max_visible_items < len(tools):
             safe_addstr(stdscr, h - 2, w//2, "v", curses.A_BOLD)

        stdscr.refresh()
        
        # Χειρισμός Εισόδου
        key = stdscr.getch()
        
        if search_mode:
            if key in [10, 13]: # Enter για επιβεβαίωση αναζήτησης
                search_mode = False
                current_selection = 0
                scroll_offset = 0
            elif key == 27: # ESC για ακύρωση αναζήτησης
                search_mode = False
                search_query = ""
                tools = list(GUIDE_DATA[category_name].keys())
                current_selection = 0
                scroll_offset = 0
            elif key in [curses.KEY_BACKSPACE, 127, 8]: # Backspace
                if len(search_query) > 0:
                    search_query = search_query[:-1]
                    tools = get_filtered_list(list(GUIDE_DATA[category_name].keys()), search_query, False)
                    current_selection = 0
                    scroll_offset = 0
            elif 32 <= key <= 126: # Εκτυπώσιμοι χαρακτήρες
                search_query += chr(key)
                tools = get_filtered_list(list(GUIDE_DATA[category_name].keys()), search_query, False)
                current_selection = 0
                scroll_offset = 0
        else:
            # Λειτουργία Πλοήγησης
            if key == ord('/'): # Ενεργοποίηση Λειτουργίας Αναζήτησης
                search_mode = True
            elif key == curses.KEY_UP:
                if current_selection > 0:
                    current_selection -= 1
                elif tools: # Τύλιγμα γύρω
                    current_selection = len(tools) - 1
            elif key == curses.KEY_DOWN:
                if current_selection < len(tools) - 1:
                    current_selection += 1
                elif tools: # Τύλιγμα γύρω
                    current_selection = 0
            elif key in [10, 13]: # Πλήκτρο Enter
                if tools:
                    selected_tool = tools[current_selection]
                    show_script_details(stdscr, selected_tool, category_name)
            elif key in [ord('q'), ord('Q'), 27]: # Έξοδος ή ESC
                break

