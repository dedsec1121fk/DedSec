def show_script_details(stdscr, script_name, category_name):
    """Εμφανίζει λεπτομερείς πληροφορίες για ένα συγκεκριμένο script χρησιμοποιώντας μια scrollable προβολή."""
    data = GUIDE_DATA[category_name][script_name]
    
    # Περιθώρια για περιθώρια
    pad_x = 2
    
    while True:
        stdscr.clear()
        h, w = stdscr.getmaxyx()
        content_width = max(10, w - (pad_x * 2))
        
        draw_header(stdscr, script_name, category=category_name)
        draw_footer(stdscr, "details")
        
        current_y = 3
        
        # Βοηθητική για εκτύπωση ενοτήτων (ενημερωμένη για ερωτήσεις FAQ)
        def print_section(title, content, color_pair, is_list=False):
            nonlocal current_y
            if current_y >= h - 2: return 
            
            # Παράλειψη κενών ή N/A ενοτήτων για FAQ
            if category_name == "Συχνές Ερωτήσεις" and title != "Περιγραφή":
                if content == "N/A" or not content or (is_list and not content):
                    return
            
            # Εκτύπωση Τίτλου
            safe_addstr(stdscr, current_y, pad_x, title.upper(), curses.color_pair(color_pair) | curses.A_BOLD)
            current_y += 1
            
            # Εκτύπωση Περιεχομένου
            if is_list and isinstance(content, list):
                for item in content:
                    if current_y >= h - 2: break
                    line = f"• {item}"
                    lines = textwrap.wrap(line, width=content_width)
                    for wrapped_line in lines:
                        if current_y >= h - 2: break
                        safe_addstr(stdscr, current_y, pad_x, wrapped_line)
                        current_y += 1
                current_y += 1
            else:
                lines = textwrap.wrap(content, width=content_width)
                for line in lines:
                    if current_y >= h - 2: break
                    safe_addstr(stdscr, current_y, pad_x, line)
                    current_y += 1
                current_y += 1 # Επιπλέον διάστημα

        # Εκτύπωση όλων των ενοτήτων (προσαρμοσμένο για FAQ)
        print_section("Περιγραφή", data["Description"], 1)
        
        # Εμφάνιση επιπλέον ενοτήτων μόνο αν δεν είναι FAQ ή αν έχουν περιεχόμενο
        if category_name != "Συχνές Ερωτήσεις" or data["How to Use"] != "N/A":
            print_section("Πώς να χρησιμοποιήσετε", data["How to Use"], 5)
        
        if category_name != "Συχνές Ερωτήσεις" or data["Save Location"] != "N/A":
            print_section("Τοποθεσία Αποθήκευσης", data["Save Location"], 6)
        
        if category_name != "Συχνές Ερωτήσεις" or data["Requirements"] != "N/A":
            print_section("Απαιτήσεις", data["Requirements"], 3)
        
        if category_name != "Συχνές Ερωτήσεις" or data["Tips"] != "N/A":
            print_section("Συμβουλές Εξπέρ", data["Tips"], 2)
        
        if "Features" in data and data["Features"]:
            print_section("Χαρακτηριστικά", data["Features"], 4, is_list=True)

        stdscr.refresh()
        
        key = stdscr.getch()
        if key in [ord('q'), ord('Q'), 27, 10, 13]: # Έξοδος ή Enter γυρίζει πίσω
            break

