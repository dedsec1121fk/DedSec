def safe_addstr(stdscr, y, x, string, attr=0):
    """
    Προσθέτει με ασφάλεια μια συμβολοσειρά στο παράθυρο, χειρίζεται περιπτώσεις 
    όπου το κείμενο μπορεί να φτάσει στην κάτω δεξιά γωνία ή να υπερβεί το πλάτος.
    """
    try:
        h, w = stdscr.getmaxyx()
        if y >= h or x >= w:
            return
        if x + len(string) > w:
            string = string[:w - x]
        stdscr.addstr(y, x, string, attr)
    except curses.error:
        pass

def draw_header(stdscr, title, search_query="", category=""):
    """Σχεδιάζει ένα στυλιζαρισμένο κεφαλίδα."""
    h, w = stdscr.getmaxyx()
    
    # Φόντο Επάνω Μπάρας
    safe_addstr(stdscr, 0, 0, " " * w, curses.color_pair(2) | curses.A_BOLD)
    header_text = f" Οδηγός Σκριπτ DedSec "
    safe_addstr(stdscr, 0, (w - len(header_text)) // 2, header_text, curses.color_pair(2) | curses.A_BOLD)
    
    # Φόντο Υποκεφαλίδας
    subheader = f" Τρέχων Προβολή: {title} "
    if category:
        subheader = f" Κατηγορία: {category} | Εργαλείο: {title} "
    if search_query:
        subheader += f"| Αναζήτηση: {search_query} "
    safe_addstr(stdscr, 1, 0, " " * w, curses.color_pair(3))
    safe_addstr(stdscr, 1, max(1, (w - len(subheader)) // 2), subheader, curses.color_pair(3))

def draw_footer(stdscr, mode="main"):
    """Σχεδιάζει υποδείξεις πλοήγησης στο κάτω μέρος."""
    h, w = stdscr.getmaxyx()
    if h > 3:
        if mode == "main":
            footer_text = " ΠΑΝΩ/ΚΑΤΩ: Πλοήγηση | ENTER: Επιλογή | /: Αναζήτηση | Q: Έξοδος "
        elif mode == "category":
            footer_text = " ΠΑΝΩ/ΚΑΤΩ: Πλοήγηση | ENTER: Επιλογή Κατηγορίας | /: Αναζήτηση | Q: Έξοδος "
        elif mode == "details":
            footer_text = " ENTER/Q: Επιστροφή στη Λίστα "
        else:
            footer_text = " ΠΑΝΩ/ΚΑΤΩ: Πλοήγηση | ENTER: Επιλογή | Q: Έξοδος "
            
        safe_addstr(stdscr, h - 1, 0, " " * w, curses.color_pair(4))
        safe_addstr(stdscr, h - 1, max(0, (w - len(footer_text)) // 2), footer_text, curses.color_pair(4))

def show_script_details(stdscr, script_name, category_name):
    """Εμφανίζει λεπτομερείς πληροφορίες για ένα συγκεκριμένο σκριπτ χρησιμοποιώντας μια κύλιση προβολή."""
    if category_name not in GUIDE_DATA or script_name not in GUIDE_DATA[category_name]:
        return

    data = GUIDE_DATA[category_name][script_name]
    
    # Περιθώρια για περιθώρια
    pad_x = 2
    
    while True:
        stdscr.clear()
        h, w = stdscr.getmaxyx()
        content_width = max(10, w - (pad_x * 2))
        
        draw_header(stdscr, script_name, category=category_name)
        draw_footer(stdscr, "details")
        
        current_y = 3
        
        # Βοηθητική για εκτύπωση τμημάτων
        def print_section(title, content, color_pair, is_list=False):
            nonlocal current_y
            if current_y >= h - 2: return 
            
            # Εκτύπωση Τίτλου
            safe_addstr(stdscr, current_y, pad_x, title.upper(), curses.color_pair(color_pair) | curses.A_BOLD)
            current_y += 1
            
            # Εκτύπωση Περιεχομένου
            if is_list and isinstance(content, list):
                for item in content:
                    if current_y >= h - 2: break
                    line = f"• {item}"
                    lines = textwrap.wrap(line, width=content_width)
                    for wrapped_line in lines:
                        if current_y >= h - 2: break
                        safe_addstr(stdscr, current_y, pad_x, wrapped_line)
                        current_y += 1
                current_y += 1
            else:
                lines = textwrap.wrap(str(content), width=content_width)
                for line in lines:
                    if current_y >= h - 2: break
                    safe_addstr(stdscr, current_y, pad_x, line)
                    current_y += 1
                current_y += 1 # Επιπλέον διάστημα

        # Εκτύπωση όλων των τμημάτων
        print_section("Περιγραφή", data.get("Περιγραφή", ""), 1)
        print_section("Τρόπος Χρήσης", data.get("Τρόπος Χρήσης", ""), 5)
        print_section("Τοποθεσία Αποθήκευσης", data.get("Τοποθεσία Αποθήκευσης", ""), 6)
        print_section("Απαιτήσεις", data.get("Απαιτήσεις", ""), 3)
        if "Συμβουλές" in data:
            print_section("Επαγγελματικές Συμβουλές", data["Συμβουλές"], 2)
        
        if "Χαρακτηριστικά" in data and data["Χαρακτηριστικά"]:
            print_section("Χαρακτηριστικά", data["Χαρακτηριστικά"], 4, is_list=True)

        stdscr.refresh()
        
        key = stdscr.getch()
        if key in [ord('q'), ord('Q'), 27, 10, 13]: # Έξοδος ή Enter επιστρέφει πίσω
            break

