def display_story_card(ιστορία):
    clear_screen()
    # Πίνακας Τίτλου
    console.print(Panel(
        Align.center(f"[bold gold1]{ιστορία['title']}[/bold gold1]\n[italic cyan]{ιστορία['source']}[/italic cyan]"),
        style="bold white on blue",
        padding=(1, 2)
    ))

    # Κείμενο Ιστορίας
    console.print(Panel(
        Text(ιστορία['story'], justify="center", style="white"),
        title="[bold]Η Ιστορία[/bold]",
        style="grey62",
        padding=(1, 2)
    ))

    rprint("\n[blink yellow]...Πατήστε ENTER για να αποκαλυφθεί η Αλήθεια...[/blink yellow]")
    input()

    # Η Αλήθεια (Μάθημα)
    console.print(Panel(
        Markdown(ιστορία['lesson']),
        title="[bold green]Η ΑΛΗΘΕΙΑ[/bold green]",
        style="green",
        padding=(1, 2)
    ))
    
    rprint("\n[dim]Πατήστε ENTER για επιστροφή...[/dim]")
    input()

def display_menu(σελίδα, ανά_σελίδα):
    clear_screen()
    console.print(Panel.fit(
        "[bold yellow]ΕΙΜΑΙ Η ΑΛΗΘΕΙΑ[/bold yellow]\n[cyan]Ορθόδοξες Ιστορίες & Σοφία[/cyan]", 
        style="bold white on blue"
    ))
    
    αρχή = (σελίδα - 1) * ανά_σελίδα
    τέλος = αρχή + ανά_σελίδα
    σύνολο_σελίδων = (len(stories) + ανά_σελίδα - 1) // ανά_σελίδα
    
    πίνακας = Table(show_header=True, header_style="bold magenta", box=None, expand=True)
    πίνακας.add_column("#", style="dim", width=4)
    πίνακας.add_column("Τίτλος", style="bold green")
    πίνακας.add_column("Πηγή", style="italic cyan")
    
    τρέχοντα_στοιχεία = stories[αρχή:τέλος]
    for i, ιστορία in enumerate(τρέχοντα_στοιχεία):
        idx = αρχή + i + 1
        πίνακας.add_row(str(idx), ιστορία['title'], ιστορία['source'])
        
    console.print(πίνακας)
    
    rprint(f"\n[dim]Σελίδα {σελίδα}/{σύνολο_σελίδων} | Σύνολο: {len(stories)}[/dim]")
    rprint("\n[bold cyan]Επιλογές:[/bold cyan]")
    rprint("[green]N[/green] = Επόμενη Σελίδα  |  [green]P[/green] = Προηγούμενη Σελίδα")
    rprint("[yellow]R[/yellow] = Τυχαία Αλήθεια |  [red]Q[/red] = Έξοδος")
    rprint("[bold white]Πληκτρολόγησε αριθμό για ανάγνωση ιστορίας[/bold white]")

    return σύνολο_σελίδων

def main():
    σελίδα = 1
    ανά_σελίδα = 30 
    
    while True:
        σύνολο_σελίδων = display_menu(σελίδα, ανά_σελίδα)
        επιλογή = Prompt.ask("\n[bold cyan]Επιλογή[/bold cyan]").lower().strip()
        
        if επιλογή == 'q':
            rprint("[bold red]Κύριε ελέησον.[/bold red]")
            break
        elif επιλογή == 'n':
            if σελίδα < σύνολο_σελίδων: σελίδα += 1
        elif επιλογή == 'p':
            if σελίδα > 1: σελίδα -= 1
        elif επιλογή == 'r':
            τυχαίος_δείκτης = random.randint(0, len(stories) - 1)
            display_story_card(stories[τυχαίος_δείκτης])
        elif επιλογή.isdigit():
            idx = int(επιλογή) - 1
            if 0 <= idx < len(stories):
                display_story_card(stories[idx])
            else:
                rprint("[red]Μη έγκυρος αριθμός![/red]")
                time.sleep(1)
        else:
            rprint("[red]Μη έγκυρη είσοδος![/red]")
            time.sleep(0.5)

if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\nΈξοδος...")
        sys.exit(0)