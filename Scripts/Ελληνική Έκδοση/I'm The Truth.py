def install(package):
    print(f"[!] Εγκατάσταση απαιτούμενης βιβλιοθήκης: {package}...")
    try:
        subprocess.check_call([sys.executable, "-m", "pip", "install", package])
        print(f"[+] Η {package} εγκαταστάθηκε με επιτυχία!")
        time.sleep(1)
    except subprocess.CalledProcessError:
        print(f"[-] Αποτυχία εγκατάστασης της {package}. Παρακαλώ εκτελέστε 'pip install {package}' χειροκίνητα.")
        sys.exit(1)

