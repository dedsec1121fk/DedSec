def install(package):
    print(f"[!] Εγκαθιστώ απαιτούμενη βιβλιοθήκη: {package}...")
    try:
        subprocess.check_call([sys.executable, "-m", "pip", "install", package])
        print(f"[+] {package} εγκαταστάθηκε επιτυχώς!")
        time.sleep(1)
    except subprocess.CalledProcessError:
        print(f"[-] Απέτυχε η εγκατάσταση {package}. Παρακαλώ εκτελέστε 'pip install {package}' χειροκίνητα.")
        sys.exit(1)

